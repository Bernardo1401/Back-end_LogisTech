CREATE DATABASE logistech;

\c logistech;

CREATE TABLE usuarios (
    id SERIAL PRIMARY KEY,
    nome VARCHAR(100) NOT NULL,
    email VARCHAR(150) UNIQUE NOT NULL,
    telefone VARCHAR(20) UNIQUE NOT NULL,
    senha TEXT NOT NULL,
    funcao VARCHAR(10) CHECK (funcao IN ('ADMIN', 'MOTORISTA')) NOT NULL,
    criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE veiculos (
    id SERIAL PRIMARY KEY,
    motorista_id INT,
    placa VARCHAR(10) UNIQUE NOT NULL,
    modelo VARCHAR(50),
    marca VARCHAR(50),
    ano INTEGER,
    FOREIGN KEY (motorista_id) REFERENCES usuarios(id)
);

CREATE TABLE pedidos (
    id SERIAL PRIMARY KEY,
    numero_pedido VARCHAR(50) UNIQUE NOT NULL,
    cliente_id INT NOT NULL,
    valor_total DECIMAL(10,2),
    endereco_cliente TEXT,
    status VARCHAR(20)
    CHECK (status IN ('PENDENTE', 'EM ROTA', 'ENTREGUE', 'CANCELADO'))
    DEFAULT 'PENDENTE',
    criado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    atualizado_em TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (cliente_id) REFERENCES usuarios(id)
);

CREATE TABLE entregas (
    id SERIAL PRIMARY KEY,
    pedido_id INT NOT NULL,
    motorista_id INT NOT NULL,
    veiculo_id INT NOT NULL,
    comprovante TEXT NOT NULL,
    status VARCHAR(20) CHECK (status IN ('PENDENTE', 'EM ROTA', 'ENTREGUE')) DEFAULT 'PENDENTE' NOT NULL,
    atribuido_em TIMESTAMP NOT NULL,
    entregue_em TIMESTAMP NOT NULL,
    FOREIGN KEY (pedido_id) REFERENCES pedidos(id),
    FOREIGN KEY (motorista_id) REFERENCES usuarios(id),
    FOREIGN KEY (veiculo_id) REFERENCES veiculos(id)
);

